# Um objeto do tipo set habilita operações matemáticas de conjuntos, 
# tais como: união, intersecção, diferença, etc. Esse tipo de estrutura 
# pode ser usado em testes de associação e remoção de valores duplicados de uma sequência.

# Uma das maneiras de se criar um objeto do tipo set é colocando os valores entre chaves, 
# conforme código a seguir:

vogais = {'a', 'e', 'i', 'o', 'u'}

# Um set permite a inserção de valores posteriores à sua criação, com a função add(), 
# mas não permite acessar valores pela sua posição.