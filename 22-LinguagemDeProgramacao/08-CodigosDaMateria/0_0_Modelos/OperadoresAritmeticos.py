a = 10
b = 5

print(f"Valores iniciais: a = {a}, b = {b}")

soma = a + b                # Operador de soma
print(f"Soma: {soma}")
print("♥   Realizando a soma entre a e b   ♥")

subtracao = a - b           # Operador de subtração
print(f"Subtração: {subtracao}")
print("♥   Realizando a subtração entre a e b   ♥")

multiplicacao = a * b       # Operador de multiplicação
print(f"Multiplicação: {multiplicacao}")
print("♥   Realizando a multiplicação entre a e b   ♥")

divisao = a / b             # Operador de divisão
print(f"Divisão: {divisao}")
print("♥   Realizando a divisão entre a e b   ♥")


potencia = a ** b           # Operador de potência
print(f"Potência: {potencia}")
print("♥   Calculando a potência de a elevado a b   ♥")

resto = a % b               # Operador de resto (módulo)
print(f"Resto da divisão: {resto}")
print("♥   Calculando o resto da divisão de a por b   ♥")

divisao_inteira = a // b    # Operador de divisão inteira
print(f"Divisão inteira: {divisao_inteira}")
print("♥   Realizando a divisão inteira de a por b   ♥")