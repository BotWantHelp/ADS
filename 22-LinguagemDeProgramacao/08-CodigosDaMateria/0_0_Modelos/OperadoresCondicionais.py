igualdade = 1 == 2
print("♥   Verificando se 1 é igual a 2   ♥")
print(f"Igualdade: {igualdade}\n")

desigualdade = 1 != 2
print("♥   Verificando se 1 é diferente de 2   ♥")
print(f"Desigualdade: {desigualdade}\n")

maior_que = 1 > 2
print("♥   Verificando se 1 é maior que 2   ♥")
print(f"Maior que: {maior_que}\n")

menor_que = 1 < 2
print("♥   Verificando se 1 é menor que 2   ♥")
print(f"Menor que: {menor_que}\n")

maior_igual = 1 >= 2
print("♥   Verificando se 1 é maior ou igual a 2   ♥")
print(f"Maior ou igual: {maior_igual}\n")

menor_igual = 1 <= 2
print("♥   Verificando se 1 é menor ou igual a 2   ♥")
print(f"Menor ou igual: {menor_igual}\n")
